public class AverageMark {
    public static void main(String[] args) {
        // Sam's marks in PCM
        int mathsMark = 94;
        int physicsMark = 95;
        int chemistryMark = 96;

        // Calculate the average
        double average = (mathsMark + physicsMark + chemistryMark) / 3.0;

        // Output the result
        System.out.printf("Sam's average mark in PCM is %.2f", average);
    }
}

